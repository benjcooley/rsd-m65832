; Trad65 - Traditional 65xx syntax test for M65832 vcore
;
; Tests the full set of traditional 65xx mnemonics in fixed32.

    .org 0x1000

start:
    BRA test_begin

pc_goal:
    NOP

test_begin:

; ===========================================================
; Test 1: LDA/LDX/LDY immediate
; ===========================================================
    LDA #100
    LDX #200
    LDY #300

; ===========================================================
; Test 2: Transfers
; ===========================================================
    TAX
    TXA
    TAY
    TYA

; ===========================================================
; Test 3: ADC/SBC immediate
; ===========================================================
    LDA #50
    ADC #25
    SBC #10

; ===========================================================
; Test 4: Traditional AND/ORA/EOR
; ===========================================================
    LDA #0xFF
    AND #0x0F
    ORA #0xF0
    EOR #0xAA

; ===========================================================
; Test 5: 3-op AND/OR/XOR
; ===========================================================
    ADDI R1, R0, #0xFF
    ADDI R2, R0, #0x0F
    AND R3, R1, R2
    OR R4, R1, R2
    XOR R5, R1, R2

; ===========================================================
; Test 6: INC/DEC
; ===========================================================
    LDA #10
    INC
    INC
    DEC

; ===========================================================
; Test 7: INX/DEX/INY/DEY
; ===========================================================
    LDX #5
    INX
    INX
    DEX
    LDY #10
    INY
    DEY

; ===========================================================
; Test 8: ASL/LSR/ROL/ROR
; ===========================================================
    LDA #1
    ASL
    ASL
    ASL A
    LSR
    LSR
    ROL
    ROR

; ===========================================================
; Test 9: Branches
; ===========================================================
    ADDI R1, R0, #5
    ADDI R2, R0, #5
    LDA R1
    CMP R2
    BEQ test9_eq
    NOP
test9_eq:
    ADDI R1, R0, #3
    ADDI R2, R0, #5
    LDA R1
    CMP R2
    BNE test9_ne
    NOP
test9_ne:

; ===========================================================
; Test 10: Large constants
; ===========================================================
    LDA #0xDEADBEEF

; ===========================================================
; Test 11: CMP/CPX/CPY
; ===========================================================
    LDA #42
    CMP #42
    LDX #100
    CPX #100
    LDY #50
    CPY #50

; ===========================================================
; Test 12: BIT
; ===========================================================
    LDA #0xFF
    BIT #0x80

; ===========================================================
; Test 13: Stack push/pull (SP-based decomposition)
; Initialize SP first to a valid RAM address
; ===========================================================
    LUI SP, 0x80010       ; SP = 0x80010000 (top of RAM area)

    LDA #0x12345678
    PHA                   ; push A onto stack
    LDA #0               ; clear A
    PLA                   ; pull A back (should be 0x12345678)

    LDX #42
    PHX                   ; push X
    LDX #0
    PLX                   ; pull X (should be 42)

    LDY #99
    PHY                   ; push Y
    LDY #0
    PLY                   ; pull Y (should be 99)

; ===========================================================
; Test 14: Flag manipulation (NOP placeholders)
; ===========================================================
    CLC
    SEC
    CLI
    SEI
    CLD
    SED
    CLV

; ===========================================================
; Test 15: System instructions
; ===========================================================
    NOP
    FENCE
    NOP

; ===========================================================
; Test 16: JSR/RTS
; ===========================================================
    JSR subroutine
    NOP
    BRA test_done

subroutine:
    LDA #42
    RTS

; ---- All tests done ----
test_done:
    BRA pc_goal
